package attractions;

public class Rollercoaster {

    private int minAge;
    private int minHeight;

    public Rollercoaster() {
        this.minAge = 12;
        this.minHeight = 145;
    }

    public int getMinAge() {
        return this.minAge;
    }


    public int getMinHeight() {
        return this.minHeight;
    }
}
